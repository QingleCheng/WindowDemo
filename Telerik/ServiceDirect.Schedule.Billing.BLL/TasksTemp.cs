using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ServiceDirect.Schedule.Billing.BLL
{
    public class TasksTemp
    {
        private Guid _keyGuid;
        private string _schedulename;
        private string _statusImageURL;
        private string _scheduleType;
        private string _next_run_date;
        private string _last_run_date;
        private string _status;
        private int _enable;
        private string _logResult;

        public TasksTemp()
        { }

        public Guid ScheduleID
        {
            get { return _keyGuid; }
            set { _keyGuid = value; }
        }

        public string LogResult
        {
            get { return _logResult; }
            set { _logResult = value; }
        }

        public string TaskName
        {
            get { return _schedulename; }
            set { _schedulename = value; }
        }


        public string StatusImageURL
        {
            get { return _statusImageURL; }
            set { _statusImageURL = value; }
        }


        public string ScheduleType
        {
            get { return _scheduleType; }
            set { _scheduleType = value; }
        }


        public string Next_run_date
        {
            get { return _next_run_date; }
            set { _next_run_date = value; }
        }


        public string Last_run_date
        {
            get { return _last_run_date; }
            set { _last_run_date = value; }
        }


        public string Status
        {
            get { return _status; }
            set { _status = value; }
        }

        public int Enable
        {
            get { return _enable; }
            set { _enable = value; }
        }
    }
}
